a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))

print("Сложение:", a + b)
print("Вычитание:", a - b)
print("Умножение:", a * b)
print("Деление:", a / b)
print("Возведение в степень:", a ** b)
print("Целочисленное деление:", a // b)
print("Остаток от деления:", a % b)
